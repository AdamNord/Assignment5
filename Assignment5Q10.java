package Assignment5Loops;

public class Assignment5Q10 {

	public static void main(String[] args) {
		
		
//			Question-10:
//			Print following output using nested for loops:
//			1
//			22
//			333
//			4444
//			55555
//			666666
//			7777777
		
		
		int row=7;
		
		for(int i=1;i<=row;i++) {
			
			for(int j=1;j<=i;j++) {
				
				System.out.print(j);
			}
			System.out.println();
		}
		
		
		
		

	}
}

